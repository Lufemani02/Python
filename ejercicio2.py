#Numeros pares
numeros = list(range(21))
print(numeros[::2])#selecciona los elementos con un paso de 2, comenzando desde el primer número

#Numeros impares
numeros1 = list(range(21))
print(numeros1[1::2])# Se usa la sintaxis de Slicing [inicio:fin:paso] para tomar solo valores impares

